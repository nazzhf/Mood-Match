document.addEventListener('DOMContentLoaded', function() {
    // DOM Elements
    const moodSelected = document.getElementById('mood-selected');
    const moviesTabContent = document.getElementById('tab-movies');
    const musicTabContent = document.getElementById('tab-music');
    const activitiesTabContent = document.getElementById('tab-activities');
    const tabButtons = document.querySelectorAll('.tab-btn');

    // Get mood from URL
    const urlParams = new URLSearchParams(window.location.search);
    const mood = urlParams.get('mood') || 'Happy'; // Default to Happy if no mood is provided

    // Display the selected mood
    if (moodSelected) {
        const lang = localStorage.getItem('language') || 'en';
        const moodKeyMap = {
            Happy: 'moodHappy',
            Sad: 'moodSad',
            Angry: 'moodAngry',
            Anxious: 'moodAnxious',
            Excited: 'moodExcited',
            Relaxed: 'moodRelaxed'
        };
        const moodKey = moodKeyMap[mood] || mood;
        moodSelected.textContent = translations[lang][moodKey] || mood;
    }

    // Function to determine the link class based on URL
    function getLinkClass(url) {
    if (typeof url !== 'string') return '';
    if (url.includes('netflix')) return 'netflix-link';
    if (url.includes('spotify')) return 'spotify-link';
    if (url.includes('disneyplus')) return 'disney-link';
    if (url.includes('amazon')) return 'amazon-link';
    if (url.includes('hulu')) return 'hulu-link';
    if (url.includes('viki')) return 'viki-link';
    return '';
    }


    // Function to get platform name from URL
    function getPlatformName(url) {
        const lang = localStorage.getItem('language') || 'en';
        
        // Check if url is undefined or null
        if (!url) {
            return translations[lang].checkItOut;
        }
        
        if (url.includes('netflix')) return translations[lang].watchNetflix;
        if (url.includes('spotify')) return translations[lang].watchSpotify;
        if (url.includes('disneyplus')) return translations[lang].watchDisneyPlus;
        if (url.includes('amazon')) return translations[lang].watchAmazon;
        if (url.includes('hulu')) return translations[lang].watchHulu;
        if (url.includes('viki')) return translations[lang].watchViki;
        if (url.includes('youtube')) return translations[lang].watchYouTube;
        if (url.includes('max')) return translations[lang].watchMax;
        if (url.includes('peacocktv')) return translations[lang].watchPeacock;
        if (url.includes('paramountplus')) return translations[lang].watchParamount;
        return translations[lang].checkItOut;
    }

    // Function to get translated description
    function getTranslatedDesc(mood, category, index, lang) {
        const key = `desc_${mood.toLowerCase()}_${category.toLowerCase()}_${index}`;
        return translations[lang][key] || recommendationsData[mood][category][index].desc;
    }

    // Function to render recommendations for a specific category
    function renderRecommendations(category, container) {
        if (!container || !recommendationsData[mood] || !recommendationsData[mood][category]) return;
        
        const recList = container.querySelector('.rec-list');
        recList.innerHTML = ''; // Clear existing content
        const lang = localStorage.getItem('language') || 'en';
    
        recommendationsData[mood][category].forEach((item, index) => {
            const li = document.createElement('li');
            li.classList.add('rec-item');
            
            const linkClass = getLinkClass(item.link);
            const platformName = getPlatformName(item.link);
            const translatedDesc = getTranslatedDesc(mood, category, index, lang);
            let translatedName = item.name;
            if (category === 'Activities') {
                const nameKey = `activity_${mood.toLowerCase()}_${index}_name`;
                translatedName = (translations[lang] && translations[lang][nameKey]) || item.name;
            }
            
            // Special handling for Activities (Self-Care) - no image
            if (category === 'Activities') {
                const activityKey = `activity_${mood.toLowerCase()}_${index}_explanation`;
                const activityExplanation = (translations[lang] && translations[lang][activityKey]) || item.explanation || translatedDesc;
                
                li.innerHTML = `
                    <div class="rec-content">
                        <div class="rec-name">${translatedName}</div>
                        <div class="rec-desc">${translatedDesc}</div>
                        <div class="rec-explanation">${activityExplanation}</div>
                    </div>
                `;
            } else {
            li.innerHTML = `
                <img src="${item.image}" alt="${item.name}" class="rec-image" onerror="this.src='https://via.placeholder.com/300x450'">
                <div class="rec-content">
                    <div class="rec-name">${translatedName}</div>
                    <div class="rec-desc">${translatedDesc}</div>
                    <a href="${item.link}" target="_blank" class="rec-link ${linkClass}">${platformName}</a>
                </div>
            `;
            }
            recList.appendChild(li);
        });
    }

    // Function to activate a specific tab
    function activateTab(tabName) {
        // Remove active class from all tabs and buttons
        document.querySelectorAll('.tab-content').forEach(tab => tab.classList.remove('active'));
        tabButtons.forEach(btn => btn.classList.remove('active'));

        // Add active class to selected tab and button
        const targetTab = document.getElementById(`tab-${tabName.toLowerCase()}`);
        const targetBtn = document.querySelector(`.tab-btn[data-tab="${tabName}"]`);
        
        if (targetTab) targetTab.classList.add('active');
        if (targetBtn) targetBtn.classList.add('active');
    }

    // Initialize the tabs
    renderRecommendations('Movies', moviesTabContent);
    renderRecommendations('Music', musicTabContent);
    renderRecommendations('Activities', activitiesTabContent);
    
    // Default to Movies tab
    activateTab('Movies');

    // Add event listeners for tab buttons
    tabButtons.forEach(button => {
        button.addEventListener('click', () => {
            const tabName = button.getAttribute('data-tab');
            activateTab(tabName);
        });
    });

    // Re-render recommendations when language changes
    document.getElementById('language-toggle').addEventListener('change', () => {
        const lang = localStorage.getItem('language') || 'en';
        const moodKeyMap = {
            Happy: 'moodHappy',
            Sad: 'moodSad',
            Angry: 'moodAngry',
            Anxious: 'moodAnxious',
            Excited: 'moodExcited',
            Relaxed: 'moodRelaxed'
        };
        const moodKey = moodKeyMap[mood] || mood;
        if (moodSelected) {
            moodSelected.textContent = translations[lang][moodKey] || mood;
        }
        renderRecommendations('Movies', moviesTabContent);
        renderRecommendations('Music', musicTabContent);
        renderRecommendations('Activities', activitiesTabContent);
    });
});
