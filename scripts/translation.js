document.addEventListener('DOMContentLoaded', function() {
    function switchLanguage(lang) {
        console.log('Switching to language:', lang); // Debugging
        localStorage.setItem('language', lang);
        document.querySelectorAll('[data-lang-key]').forEach(element => {
            const key = element.getAttribute('data-lang-key');
            const content = translations[lang][key] || translations.id[key];
            
            if (content) {
                element.textContent = content;
            } else {
                console.log(`${key} language not defined`);
            }
        });
        document.body.classList.add('language-loaded'); // Tandai bahasa sudah dimuat
    }
    
    const preferredLang = localStorage.getItem('language') || 'id';
    console.log('Preferred language:', preferredLang); // Debugging
    switchLanguage(preferredLang);

    const languageToggle = document.getElementById('language-toggle');
    languageToggle.value = preferredLang;
    languageToggle.addEventListener('change', (e) => switchLanguage(e.target.value));
});